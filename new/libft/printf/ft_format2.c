#include "../libft.h"

void	ft_di(int n, int *i)
{
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		(*i) += 11;
		return ;
	}
	if (n < 0)
	{
		write(1, "-", 1);
		(*i)++;
		n *= -1;
	}
	if (n >= 10)
		ft_di(n / 10, i);
	write(1, &"0123456789"[n % 10], 1);
	(*i)++;
}

void	ft_u(t_ui u, int *i)
{
	if (u >= 10)
		ft_u(u / 10, i);
	write(1, &"0123456789"[u % 10], 1);
	(*i)++;
}
