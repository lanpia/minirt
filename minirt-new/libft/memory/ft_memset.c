#include "../libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*p;
	unsigned char	v;
	size_t			i;

	i = 0;
	p = (unsigned char *)b;
	v = (unsigned char)c;
	while (i < len)
		p[i++] = v;
	return (b);
}
